const luckyBtn = document.querySelector('.lucky__btn');
const logo = document.querySelector('.logo');
const searchField = document.querySelector('.search__field');
const spacer = document.querySelector('.spacer');
const clearBtn = document.querySelector('.clear__btn');
luckyBtn.addEventListener('click', onLuckyBtn);

function onLuckyBtn () {
    const markupOddity = addOddityLogo();
    logo.classList.toggle('logoOddity');
    if (logo.classList.contains('logoOddity')) {
        gsap.from('.logo',  { opacity: 0, duration: 1, delay: 0.1, y: 30 });
        logo.innerHTML = markupOddity
    } else {
        const markupGoogle = addGoogleLogo();
        gsap.from('.logo',  { opacity: 0, duration: 1, delay: 0.1, y: 30 });
        logo.innerHTML = markupGoogle;
    }
}

searchField.addEventListener('keydown', onInputFocus);
function onInputFocus () {
    if (searchField.value.length) {
        spacer.classList.remove('disabled');
        clearBtn.classList.remove('disabled');
    } else {
        spacer.classList.add('disabled');
        clearBtn.classList.add('disabled');
    }
}

clearBtn.addEventListener('click', onClearBtn)
function onClearBtn () {
    searchField.value = ""
}

